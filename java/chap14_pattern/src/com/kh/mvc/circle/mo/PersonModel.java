package com.kh.mvc.circle.mo;

public class PersonModel {
	private String name;
	private int age;



public PersonModel(String name, int age) {
	this.name = name;
	this.age = age;
}



public String getname() {
	return name;
}



public void setname(String name) {
	this.name= name;
}



public int getage() {
	return age;
}



public void setage(int age) {
	this.age = age;
}




}
