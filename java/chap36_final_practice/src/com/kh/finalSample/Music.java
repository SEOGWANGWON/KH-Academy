package com.kh.finalSample;

public class Music {
	private String name;
	private String heart;
	
	
	public Music(String name, String heart) {
		this.name = name;
		this.heart = heart;
	}
	public String getName() {
		return name;
	}
	public String getHeart() {
		return heart;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setHeart(String heart) {
		this.heart = heart;
	}
	
}
