SELECT student_no AS "학번", student_name AS "이름", entrance_date AS "입학년도"
from tb_student 
WHERE DEPARTMENT_NO = 002
ORDER BY DEPARTMENT_NO ASC;



--2번 문제
SELECT PROFESSOR_NAME AS "교수이름", PROFESSOR_SSN AS "주민등록번호"
FROM TB_PROFESSOR
WHERE LENGTH(PROFESSOR_NAME) > 3 OR LENGTH(PROFESSOR_NAME) < 3
ORDER BY PROFESSOR_NAME ASC;


--3번 문제
SELECT PROFESSOR_NAME AS "교수이름", 2008 - (19||SUBSTR(PROFESSOR_SSN,1,2)) AS "나이"
FROM TB_PROFESSOR
WHERE SUBSTR(PROFESSOR_SSN,8,1) = 1
ORDER BY 나이 ASC;


