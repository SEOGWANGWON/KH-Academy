--5번 문제 ( 내 생각엔 맞는 풀이 방식인데 답 이 다름 )
SELECT STUDENT_NO, STUDENT_NAME, STUDENT_SSN, ENTRANCE_DATE
FROM TB_STUDENT
WHERE ((20||SUBSTR(ENTRANCE_DATE,1,2)) - (19||SUBSTR(STUDENT_SSN,1,2))) > 19
ORDER BY STUDENT_NO DESC ;



